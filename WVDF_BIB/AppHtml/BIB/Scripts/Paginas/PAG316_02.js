// AUTOCOMPLETE DESCRI플O PARA POSICIONAR ID - MARCAS INICIO
$(function() {
    $("#csag374_des_i").autocomplete({
        source: function (request, response) {
			$.ajax({
				url: "fbcsag308_descricao.asp",
				data: {
					aUsuarioSessao: $('#jaUsuarioSessao').val(),
					term: retiraAcento(request.term)
				},
				dataType: "json",
				success: function (data) {
					response(data);
				}
			});
		},
        minLength: 2,
		open: function(e,ui) {
			var termTemplate = '<strong>%s</strong>';
			var acData = $(this).data('uiAutocomplete');
			acData
			.menu
			.element
			.find('a')
			.each(function() {
				var me = $(this);
				var regex = new RegExp(acData.term, "gi");
				me.html(me.text().replace(regex, function (matched) {
					return termTemplate.replace('%s', matched);
				}));
			});
		},
        select: function(event, ui) {
            if (ui.item) {
				$('#SelStart2').val(ui.item.id);
				$('#SelStop2').val(ui.item.id);
				$('#csag374_des_f').val(ui.item.value);
				$(this).val(ui.item.value);
				return false;
			}
        }
	}).autocomplete("instance")._renderItem = function(ul, item) {
		return $("<li>").append("<a>" + item.value + " - " + item.cidade + "</a>").appendTo(ul);
	};
});

